package ex1;

public class ArithmeticOparator {
    public static void main(String[] args) {
        //算術演算子
        int a = 5,b = 3;
        System.out.println(a + " + " + b + " = " + (a+b));
        System.out.println(a + " - " + b + " = " + (a-b));
        System.out.println(a + " * " + b + " = " + (a*b));
        System.out.println(a + " / " + b + " = " + (a/b));
        System.out.println(a + " % " + b + " = " + (a%b));

        double c = 5,d = 3;
        System.out.println(c + " + " + d + " = " + (c+d));
        System.out.println(c + " - " + d + " = " + (c-d));
        System.out.println(c + " * " + d + " = " + (c*d));
        System.out.println(c + " / " + d + " = " + (c/d));
        System.out.println(c + " % " + d + " = " + (c%d));
    }
}
